import java.util.*;
public class TypeConvAndCasting {
    public static void main(String args[]) {
        // long a = 25;  // Declares `a` as long
        // int b = (int) a;  // Explicitly casts `long` to `int`

        int a = 25;  // Declare `a` as an int
        int b = a;   // Assign directly since both are int

        System.out.println(b);
    }
}
