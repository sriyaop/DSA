public class JavaBasics {
    public static void main(String args[]) {
        byte b = 8;
        System.out.println(b);
        
        char ch = 'A';
        System.out.println(ch);

        boolean var = true;
        System.out.println(var);

        float price = 10.5f; // Add 'f' to specify float literal otherwise java considers ad double by default and error will be thown coz we declared as float
        System.out.println(price);

        long largeNumber = 123456789L; // 'L' for long literals
        System.out.println(largeNumber);

        double preciseValue = 20.12345;
        System.out.println(preciseValue);

        short smallNumber = 120;
        System.out.println(smallNumber);
    }
}
